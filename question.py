# Assign values to variables a and b
a = 20  # You can change this value to any number
b = 18   # You can change this value to any number

# Addition of a and b
addition = a + b
print("Addition of a and b:", addition)  # Output: 38

# Subtraction of a and b
subtraction = a - b
print("Subtraction of a and b:", subtraction)  # Output: 2

# Multiplication of a and b
multiplication = a * b
print("Multiplication of a and b:", multiplication)  # Output: 360

# Division of a and b
division = a / b  # Ensure b is not zero to avoid division by zero error
print("Division of a and b:", division)  # Output: 1.1111111111111112

